public class v5 {
    public static void main(String[] args) {

        int num1=2;
        int num2=3;
        System.out.println("num1:  " + num1);
        System.out.println("num2 : " + num2);
        int temp; //variable used to swap two numbers
        temp=num1;
        num1=num2;
        num2=temp;
        System.out.println("num1 : " + num1);
        System.out.println("num2 : " + num2);
    }
}
